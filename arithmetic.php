<?php

class Arithmetic{

public $num1;
public $num2;



public function add_numbers()
{

return $this->num1+$this->num2;

}
public function subtract_numbers()
{

return $this->num1-$this->num2;

}
public function multiply_numbers()
{

return $this->num1*$this->num2;

}
public function divide_numbers()
{

return $this->num1/$this->num2;

}

}


?>